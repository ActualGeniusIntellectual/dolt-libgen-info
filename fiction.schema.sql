CREATE TABLE `fiction` (
  `ID` int unsigned NOT NULL AUTO_INCREMENT,
  `MD5` char(32) CHARACTER SET ascii COLLATE ascii_general_ci DEFAULT NULL,
  `Title` varchar(2000) DEFAULT '',
  `Author` varchar(300) DEFAULT '',
  `Series` varchar(300) DEFAULT '',
  `Edition` varchar(50) DEFAULT '',
  `Language` varchar(45) DEFAULT '',
  `Year` varchar(10) DEFAULT '',
  `Publisher` varchar(100) DEFAULT '',
  `Pages` varchar(10) DEFAULT '',
  `Identifier` varchar(400) DEFAULT '',
  `GooglebookID` varchar(45) DEFAULT '',
  `ASIN` varchar(10) DEFAULT '',
  `Coverurl` varchar(200) DEFAULT '',
  `Extension` varchar(10) DEFAULT '',
  `Filesize` int unsigned DEFAULT '0',
  `Library` varchar(50) DEFAULT '',
  `Issue` varchar(100) DEFAULT '',
  `Locator` varchar(512) DEFAULT '',
  `Commentary` varchar(500) DEFAULT NULL,
  `Generic` char(32) DEFAULT '',
  `Visible` char(3) DEFAULT '',
  `TimeAdded` timestamp DEFAULT CURRENT_TIMESTAMP,
  `TimeLastModified` timestamp DEFAULT NULL,
  PRIMARY KEY (`ID`),
  FULLTEXT KEY `Authors` (`Author`),
  FULLTEXT KEY `Identifier` (`Identifier`),
  KEY `Language` (`Language`),
  UNIQUE KEY `MD5UNIQUE` (`MD5`),
  FULLTEXT KEY `Series` (`Series`),
  FULLTEXT KEY `TITLE` (`Title`),
  FULLTEXT KEY `Title+Authors+Series` (`Title`,`Author`,`Series`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci
